package com.company;

public class Datos {

    String nombre, profesion, añosDeExpriencia, pais;

    public  Datos(){

    }

    public String getNombre() {
        return nombre;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }

    public String getProfesion() {
        return profesion;
    }

    public void setProfesion(String profesion) {
        this.profesion = profesion;
    }

    public String getAñosDeExpriencia() {
        return añosDeExpriencia;
    }

    public void setAñosDeExpriencia(String añosDeExpriencia) {
        this.añosDeExpriencia = añosDeExpriencia;
    }
    public String getPais(){
        return pais;
    }
    public void  setPais(String pais){
        this.pais=pais;
    }
}

